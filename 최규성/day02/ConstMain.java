package day02;

public class ConstMain {

	public static void main(String[] args) {
		
		int r = 10;
		final double PI = 3.14; //변수의  상수화(고정), final이 붙으면 변수를 전부 대문자로 쓴다.
		
		System.out.println("원둘레: " + ( 2 * r * PI) );
		
		System.out.println("\\ \" \n \t \' "); //이스케이프 문자
		
		
		
	}

}
